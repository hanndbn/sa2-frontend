    <h1 id="introduction" class="intro-header">Giới thiệu chung</h1>

    <p>Chính thức thành lập ngày 20/07/1994, với tiền thân là Trung tâm Thí nghiệm Mạng Netlab, Công ty Cổ phần Công nghệ Tinh Vân đã trải qua 20 năm hình thành và phát triển, với tập thể lãnh đạo đam mê và gắn kết, đội ngũ nhân viên nhiệt huyết và chuyên nghiệp, văn hóa doanh nghiệp đặc trưng, lịch sử tăng trưởng ổn định, Tinh Vân đã luôn khẳng định mình là một trong những đơn vị đi đầu trong lĩnh vực công nghệ thông tin, đặc biệt là phần mềm và nội dung số tại Việt Nam.</p>

    <p>Từ 03 sáng lập viên đầu tiên, đến nay Tinhvan Group đã có 6 công ty thành viên cùng 4 chi nhánh tại Tp. HCM, với tổng số 500 nhân viên trẻ trung, giàu kinh nghiệm, sáng tạo, có trình độ kỹ thuật cao. Với phong cách kinh doanh chuyên nghiệp và thân thiện, Tinh Vân luôn nhận được những đánh giá cao cùng sự hài lòng từ phía các khách hàng và đối tác.</p>

    <b>Lĩnh vực hoạt động</b>

    <ul>
      <li>Phát triển các sản phẩm phần mềm, xây dựng các giải pháp CNTT chuyên nghiệp cho khách hàng thuộc khối Chính phủ và Giáo dục.</li>
      <li>Tư vấn, triển khai ERP, HRM và các giải pháp CNTT khác cho Doanh nghiệp.</li>
      <li>Gia công và xuất khẩu phần mềm.</li>
      <li>Phát triển các dịch vụ trực tuyến, ứng dụng trên mobile và nội dung số. </li>
      <li>Kinh doanh và phát triển game cho mobile.</li>
      <li>Kinh doanh và phát triển công nghệ sách điện tử.</li>
    </ul>

    <p>Tinh Vân nhận được những giải thưởng uy tín trong lĩnh vực CNTT tại Việt Nam trong nhiều năm liên tục từ 2002 đến 2013 như: Sao Khuê, Cúp vàng CNTT, TOP5 ICT, Giải thưởng CNTT-TT Tp. HCM, Bằng khen của Bộ trưởng Bộ Thông tin và Truyền thông… Sản phẩm và dịch vụ của Tinh Vân luôn giành được những giải thưởng cao nhất của Hội Tin học Việt Nam, Hội Tin học Tp. HCM và Hiệp hội Doanh nghiệp Phần mềm Việt Nam.</p>


    <p>Toàn bộ lãnh đạo và nhân viên Tinh Vân cam kết không ngừng phấn đấu, phát huy trí tuệ và sức sáng tạo, tập trung tối đa sức mạnh đoàn kết tập thể, mang lại cho khách hàng sự hài lòng và những giá trị nổi trội thông qua các sản phẩm và dịch vụ hoàn hảo, trở thành một trong những doanh nghiệp hàng đầu trong thời đại hội nhập ngày nay.</p>

    <h3 id="introduction1">Tầm nhìn</h3>
    <p><em>“Tinh Vân định hướng thành một tập đoàn công nghệ hàng đầu, phát triển bền vững trên nền tảng của sức mạnh tri thức và tính nhân bản, vươn ra thị trường toàn cầu, nỗ lực đóng góp cộng đồng và phát huy tối đa tài năng, sức sáng tạo cho mỗi thành viên.”</em></p>


    <h3 id="introduction2">Chiến lược</h3>
    <p>Trong chặng đường phát triển 2010-2015, tất cả các đơn vị thành viên của Tinh Vân đang được định hướng phát triển với chiến lược “Mass hóa”. Phục vụ cho hàng ngàn tổ chức, cho hàng triệu người Việt Nam và vươn tới thị trường toàn cầu là mục tiêu và niềm cảm hứng của chúng tôi. Để phục vụ cho chiến lược này, hiện tại Tinh Vân đang chú trọng đầu tư cho những công nghệ nền tảng như Cloud, chuyển hướng phát triển sản phẩm sang SaaS, cũng như tập trung mạnh hơn vào thị trường internet và công nghệ mobile.</p>
    
    <h3 id="introduction3">Khẩu hiệu</h3>         
    <p style="text-align: justify;">Mỗi năm Tinhvan Group đều có một khẩu hiệu hành động, được HĐQT cân nhắc lựa chọn sao cho phù hợp nhất với chiến lược phát triển, với bối cảnh kinh tế vĩ mô cũng như tình hình nội tại của công ty. Khẩu hiệu này là tinh thần xuyên suốt cho hoạt động của tất cả các đơn vị thành viên, phòng ban trong Tinh Vân, và thực tế đã góp phần không nhỏ giúp Tinh Vân vượt qua những thời điểm khó khăn, phát huy tối đa nội lực, nắm bắt nhanh chóng các cơ hội và phát triển bền vững trong suốt thời gian qua.</p>

    <ul>
      <li><em><strong>Các từ khóa 2013: "Kỷ luật", "Sáng tạo, "Hệ sinh thái”</strong></em></li>
    </ul>

    <p>Năm 2013, Tinh Vân sẽ tiếp phát huy khẩu hiệu của năm 2012 và từng bước tạo ra một “Văn hóa <strong>Kỷ luật</strong>” - một Văn hóa không nhằm kỷ luật con người, mà giúp cho các cá nhân làm việc trong môi trường phải tự thích nghi, tự trọng và tự kiểm soát. Để Tinh Vân có thể tồn tại, vượt qua và phát triển mạnh hơn nữa trong năm 2013.</p>

    <p>Năm 2013 cũng là năm Tinh Vân sẽ phát huy tối đa sức <strong>Sáng tạo</strong> tiềm tàng trong mỗi cá nhân, mạnh dạn áp dụng các ý tưởng mới trong mỗi công việc dù nhỏ nhất. Bởi trong một thị trường CNTT, công nghệ phần mềm và nội dung số đầy biến động, cách duy nhất để tồn tại chỉ là <strong>Sáng tạo</strong> và <strong>Sáng tạo</strong> không ngừng.</p>

    <p>Năm 2013 đánh dấu giai đoạn cơ cấu Tinhvan Group đã hoàn tất. Tinh Vân trở thành một <strong>Hệ sinh thái </strong>Công nghệ với 6 công ty thành viên hoạt động vững vàng trên thị trường phần mềm/nội dung số với các nền tảng công nghệ đa dạng. Trong bối cảnh kinh tế còn rất xấu, mỗi doanh nghiêp đơn lẻ đang phải chịu những rủi ro cao, nhưng cả <strong>Hệ sinh thái</strong> sẽ có sức sống mãnh liệt, cũng như cái tổng thể sẽ lớn và mạnh mẽ hơn rất nhiều tổng của những thực thể đơn lẻ.</p>

    <ul>
      <li><strong><em>Khẩu hiệu 2012 “Kỷ luật để vượt trội”</em></strong></li>
    </ul>

    <p>Khẩu hiệu “Kỷ luật để vượt trội” của năm 2012 thể hiện ý chí quyết tâm xây dựng một “Văn hóa kỷ luật” mạnh tại Tinh Vân. “Văn hóa kỷ luật” sẽ không nhằm vào việc kỷ luật con người mà hướng tới một tình yêu sâu sắc đối với công việc, sự tự kiểm soát và lòng tự trọng. “Văn hóa kỷ luật” sẽ phải được hình thành, lan tỏa và tạo cảm hứng từ những lãnh đạo. Tinh Vân tin tưởng rằng khẩu hiệu “Kỷ luật để vượt trội” sẽ giúp từng bước hoàn thiện kỷ cương quản trị (Corporate Governance), vượt qua cơn bão khủng hoảng 2012 và tiếp tục vươn lên mạnh mẽ.</p>

    <ul>
      <li><strong><em>Khẩu hiệu 2011 “Cày sâu cuốc bẫm”</em></strong></li>
    </ul>

    <p>Các mảng kinh doanh của Tinh Vân hiện tại giống những thửa ruộng tốt lành, trong những năm qua đã được qui hoạch chuẩn mực. Đánh giá tình hình khó khăn của 2011, Tinh Vân cần phải có một sự tập trung cao độ vào việc làm thật tốt chuyên môn, phát huy tối đa kinh nghiệm và sở trường của mình, đó chính là tinh thần “Cày sâu cuốc bẫm”. Khẩu hiệu này đã giúp Tinh Vân vẫn tăng trưởng tốt trong bối cảnh kinh tế hết sức bất lợi.</p>

    <ul>
      <li><strong><em>Khẩu hiệu 2010 “Giải phóng tiềm năng”</em></strong></li>
    </ul>

    <p>“Giải phóng tiềm năng” đối với nhân viên là khai thác tối đa năng lực làm việc thông qua hệ thống KPI, đối với quản lý và lãnh đạo là căn cứ vào hiệu quả về lợi nhuận thông qua qui chế tài chính và chính sách lương thưởng tốt. Đối với các thành viên HĐQT, “Giải phóng” đồng nghĩa với việc tách biệt văn hóa sở hữu và quản trị với văn hóa điều hành, các thành viên HĐQT không nhất thiết phải giữ các vị trí điều hành trọng yếu.</p>

