    <h3 id="introduction4">Giá trị cốt lõi</h3>              
    <p style="text-align: justify;">Từ khi thành lập đến nay Tinh Vân đã được xây dựng và phát triển bền vững trên cơ sở của 3 giá trị cốt lõi cơ bản đã được kiểm nghiệm qua thời gian, đó là Văn hóa Tinh Vân, Lãnh đạo Tinh Vân và Công nghệ Tinh Vân.</p>
    <p style="text-align: justify;"><strong>Về Văn hóa</strong>, Tinh Vân tự hào đã bền bỉ xây dựng thành công một môi trường văn hóa doanh nghiệp đặc trưng, mang đậm tình thân ái, tính nhân bản, sáng tạo, hài hước, giàu về trí tuệ và tinh thần. Văn hóa Tinh Vân đã giúp các nhân viên luôn ý thức về chất lượng của sản phẩm và dịch vụ nhằm đảm bảo sự hài lòng cao nhất của khách hàng, cũng như giúp các đồng nghiệp quí mến, chia sẻ và gắn bó với tổ chức.</p>
    <p style="text-align: justify;"><strong>Về Lãnh đạo</strong>, Tinh Vân cũng tự hào có rất nhiều con người xuất sắc, luôn hết lòng với công việc, kiên định, cùng chia sẻ một tầm nhìn rộng lớn về tương lai, gắn bó, đoàn kết, đam mê và đầy tài năng.</p>
    <p style="text-align: justify;"><strong>Về Công nghệ</strong>, Tinh Vân là doanh nghiệp tiên phong trong nhiều lĩnh vực của công nghệ thông tin và phần mềm, với giá trị được tích lũy trong hơn 15 năm qua, bao gồm know-how cùng nhiều bằng sáng chế và bản quyền của các công nghệ bản địa, đặc biệt liên quan đến công nghệ tìm kiếm, bảo mật, xử lý ngôn ngữ tiếng Việt, dịch tự động, nhận dạng, cùng nhiều công nghệ nền khác…</p>
